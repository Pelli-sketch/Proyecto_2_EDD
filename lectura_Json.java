class FamilyMember {
    String name;
    String[] bornTo;
    String[] fatherOf;
    String known_As;
    String heldTitle;
    String wedTo;
    String eyeColor;
    String hairColor;
    String house;
    String notes;
    String fate;

    public FamilyMember(String name, String[] bornTo, String[] fatherOf, String known_As, String heldTitle, String wedTo, String eyeColor, String hairColor,  String house, String notes, String fate) {
        this.name = name;
        this.bornTo = bornTo;
        this.fatherOf = fatherOf;
        this.known_As = known_As;
        this.wedTo = wedTo;
        this.eyeColor = eyeColor;
        this.hairColor = hairColor;
        this.heldTitle = heldTitle;
        this.house = house;
        this.notes = notes;
        this.fate = fate;
    }
}


